def build_markdown(
    recon: NormalizedRecon,
    summary: PTAssessmentSummary,
    mapping: list[OwaspMappingItem],
    assignments: list[PTTestAssignment],
    vectors: list[AttackVector],
) -> str:
    """Render the PT plan as Markdown."""
    caps = recon.capabilities
    lines: list[str] = []

    # ---- Header ----
    lines.append("# AI Agent Penetration Testing Plan")
    lines.append("")
    lines.append("_Generated by ai-agents-recon Phase 2-4 (deterministic + CrewAI agents)._")
    lines.append("")

    # ---- Target Summary ----
    lines.append("## Target Summary")
    lines.append("")
    lines.append(f"- **Target name**: `{summary.target_name}`")
    lines.append(f"- **Target type**: `{summary.target_type}`")
    lines.append(f"- **Overall risk**: **{summary.overall_risk}**")
    lines.append(
        "- **Key capabilities**: "
        + ", ".join(
            f"`{flag}`"
            for flag, value in (
                ("has_tools", caps.has_tools),
                ("has_mcp", caps.has_mcp),
                ("has_memory", caps.has_memory),
                ("has_rag", caps.has_rag),
                ("can_execute_code", caps.can_execute_code),
                ("can_call_external_apis", caps.can_call_external_apis),
                ("can_modify_data", caps.can_modify_data),
                ("multi_agent", caps.multi_agent),
                ("has_human_approval", caps.has_human_approval),
            )
            if value
        )
        or "_none confirmed_"
    )
    lines.append(f"- **Permission scope**: `{caps.permission_scope}`")
    lines.append(f"- **Identity model**: `{caps.identity_model}`")
    if summary.top_focus_areas:
        lines.append("- **Top focus areas**:")
        for f in summary.top_focus_areas:
            lines.append(f"  - {f}")
    if summary.reason:
        lines.append(f"- **Reason**: {summary.reason}")
    lines.append("")

    # ---- OWASP Mapping ----
    lines.append("## OWASP Agentic AI Mapping")
    lines.append("")
    lines.append(
        "_Reference: https://www.promptfoo.dev/docs/red-team/owasp-agentic-ai/_"
    )
    lines.append("")
    lines.append("| ID | Name | Applicable | Priority | Confidence | Score |")
    lines.append("|---|---|---|---|---|---|")
    for m in mapping:
        applicable = "Yes" if m.applicable else "-"
        lines.append(
            f"| {m.owasp_id} | {m.name} | {applicable} | {m.priority} | "
            f"{m.confidence:.2f} | {m.score_breakdown.total} |"
        )
    lines.append("")

    for m in mapping:
        if not m.applicable:
            continue
        lines.append(f"### {m.owasp_id} - {m.name}")
        lines.append("")
        lines.append(f"- **Priority**: {m.priority}  -  **Confidence**: {m.confidence:.2f}")
        lines.append(
            f"- **Score**: {m.score_breakdown.total} "
            f"(impact={m.score_breakdown.impact}, "
            f"exploitability={m.score_breakdown.exploitability}, "
            f"exposure={m.score_breakdown.exposure}, "
            f"privilege={m.score_breakdown.privilege}, "
            f"autonomy={m.score_breakdown.autonomy}, "
            f"approval_control=-{m.score_breakdown.approval_control})"
        )
        lines.append(f"- **Rationale**: {m.rationale}")
        lines.append("- **Matched recon signals**:")
        lines.append(_bullet(_safe(m.matched_recon_signals)))
        lines.append("- **Recommended test focus**:")
        lines.append(_bullet(_safe(m.recommended_test_focus)))
        lines.append("")

    # ---- Prioritized Attack Vectors ----
    lines.append("## Prioritized Attack Vectors")
    lines.append("")
    if not vectors:
        lines.append("_No attack vectors generated (no applicable categories)._")
        lines.append("")
    else:
        by_asi: dict[str, list[AttackVector]] = {}
        for v in vectors:
            by_asi.setdefault(v.owasp_id, []).append(v)
        order = [m.owasp_id for m in mapping if m.applicable]
        for owasp_id in order:
            vs = by_asi.get(owasp_id, [])
            if not vs:
                continue
            lines.append(f"### {owasp_id} vectors")
            lines.append("")
            for v in vs:
                lines.append(f"#### `{v.id}` {v.title}")
                lines.append("")
                lines.append(f"- **Priority**: {v.priority}")
                lines.append(f"- **OWASP**: {v.owasp_id}")
                lines.append(f"- **Objective**: {v.objective}")
                if v.recon_basis:
                    lines.append("- **Recon basis**:")
                    lines.append(_bullet(_safe(v.recon_basis)))
                lines.append(f"- **Attack scenario**: {v.attack_scenario}")
                if v.preconditions:
                    lines.append("- **Preconditions**:")
                    lines.append(_bullet(_safe(v.preconditions)))
                if v.test_steps:
                    lines.append("- **Test steps**:")
                    lines.append(_bullet(_safe(v.test_steps)))
                if v.safe_payload_examples:
                    lines.append("- **Safe payload examples**:")
                    lines.append(
                        "\n".join(f"  - `{p}`" for p in _safe(v.safe_payload_examples))
                    )
                lines.append(f"- **Expected secure behavior**: {v.expected_secure_behavior}")
                lines.append(f"- **Vulnerable behavior**: {v.vulnerable_behavior}")
                if v.evidence_to_collect:
                    lines.append("- **Evidence to collect**:")
                    lines.append(_bullet(_safe(v.evidence_to_collect)))
                lines.append(f"- **Risk if successful**: {v.risk_if_successful}")
                if v.recommended_controls:
                    lines.append("- **Recommended controls**:")
                    lines.append(_bullet(_safe(v.recommended_controls)))
                lines.append(
                    f"- **Execution mode**: `{v.execution_mode}`  -  "
                    f"**Destructive**: `{v.destructive}`"
                )
                lines.append("")

    # ---- Execution Notes ----
    lines.append("## Execution Notes")
    lines.append("")
    lines.append(
        "- **Scope**: Only the target identified above, only the capabilities listed in "
        "this report, and only in the engagement-approved environment."
    )
    lines.append(
        "- **Safe testing rules**: Every vector here is non-destructive. Use only the "
        "marker `PT_TEST_TOKEN_DO_NOT_EXECUTE`, safe commands (`whoami`, `hostname`), "
        "test recipients, and dry-run modes. Do NOT escalate beyond the documented steps."
    )
    lines.append(
        "- **Required approvals**: Confirm written authorization to assess the target "
        "before execution."
    )
    lines.append(
        "- **Logging and evidence**: Capture full request/response bodies, tool-call "
        "traces, authorization decisions, and timestamps. Preserve session ids."
    )
    lines.append(
        "- **Backout / rollback**: For data-modification vectors, use isolated test "
        "records only and restore state immediately after each test."
    )
    lines.append("")

    return "\n".join(lines)


