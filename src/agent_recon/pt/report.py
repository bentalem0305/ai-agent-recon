# ---------------------------------------------------------------------------
# Markdown helpers
# ---------------------------------------------------------------------------

def _bullet(items: list[str]) -> str:
    return "\n".join(f"- {x}" for x in items) if items else "- _none_"


def _safe(items: list[str]) -> list[str]:
    return [str(i) for i in items if isinstance(i, str) and i.strip()]


