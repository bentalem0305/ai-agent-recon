def _priority_class(p: str) -> str:
    return _PRIORITY_CLASS.get(p, "sev-info")  # type: ignore[arg-type]


def _bullets_html(items: list[str]) -> str:
    items = _safe(items)
    if not items:
        return '<p class="muted small">None.</p>'
    return "<ul>" + "".join(f"<li>{_html_escape(x)}</li>" for x in items) + "</ul>"


def _render_owasp_table(mapping: list[OwaspMappingItem]) -> str:
    rows: list[str] = []
    for m in mapping:
        applicable_pill = (
            "<span class='pill conf-high'>yes</span>"
            if m.applicable
            else "<span class='pill conf-uncertain'>no</span>"
        )
        priority_pill = (
            f"<span class='pill {_priority_class(m.priority)}'>"
            f"{_html_escape(m.priority)}</span>"
        )
        rows.append(
            "<tr>"
            f"<td class='mono small'>{_html_escape(m.owasp_id)}</td>"
            f"<td>{_html_escape(m.name)}</td>"
            f"<td>{applicable_pill}</td>"
            f"<td>{priority_pill}</td>"
            f"<td class='mono small'>{m.confidence:.2f}</td>"
            f"<td class='mono small'>{m.score_breakdown.total}</td>"
            "</tr>"
        )
    return (
        "<div class='scroll-wrap'><table class='data-table'>"
        "<thead><tr>"
        "<th>ID</th><th>Name</th><th>Applicable</th>"
        "<th>Priority</th><th>Confidence</th><th>Score</th>"
        "</tr></thead>"
        f"<tbody>{''.join(rows)}</tbody></table></div>"
    )


def _render_owasp_cards(mapping: list[OwaspMappingItem]) -> str:
    cards: list[str] = []
    for m in mapping:
        if not m.applicable:
            continue
        sev = _priority_class(m.priority)
        sb = m.score_breakdown
        cards.append(
            f"<article class='risk-card {sev}' id='owasp-{_html_escape(m.owasp_id.lower())}'>"
            "<header class='risk-header'>"
            f"<span class='pill {sev}'>{_html_escape(m.priority)}</span>"
            f"<h3>{_html_escape(m.owasp_id)} &middot; {_html_escape(m.name)}</h3>"
            f"<span class='pill conf-medium'>conf: {m.confidence:.2f}</span>"
            "</header>"
            f"<p class='risk-desc'>{_html_escape(m.rationale)}</p>"
            "<div class='risk-grid'>"
            "<div>"
            "<h4>Matched recon signals</h4>"
            f"{_bullets_html(m.matched_recon_signals)}"
            "</div>"
            "<div>"
            "<h4>Recommended test focus</h4>"
            f"{_bullets_html(m.recommended_test_focus)}"
            "<h4>Score breakdown</h4>"
            f"<p class='score-detail'>total={sb.total} "
            f"(impact={sb.impact}, exploit={sb.exploitability}, "
            f"exposure={sb.exposure}, privilege={sb.privilege}, "
            f"autonomy={sb.autonomy}, approval=-{sb.approval_control})</p>"
            "</div>"
            "</div></article>"
        )
    return "\n".join(cards) if cards else '<p class="muted">No applicable categories.</p>'


