def _dump(obj, path: Path) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        if hasattr(obj, "model_dump"):
            json.dump(obj.model_dump(mode="json"), f, indent=2, ensure_ascii=False)
        else:
            json.dump(obj, f, indent=2, ensure_ascii=False, default=str)
    return path


def write_pt_outputs(
    output_dir: str | Path,
    recon: NormalizedRecon,
    mapping: list[OwaspMappingItem],
    summary: PTAssessmentSummary,
    assignments: list[PTTestAssignment],
    vectors: list[AttackVector],
) -> list[Path]:
    """Write the six canonical files into ``output_dir`` and return their paths."""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    paths: list[Path] = []
    paths.append(_dump(recon, out / "normalized-recon.json"))

    paths.append(
        _dump(
            {"owasp_mapping": [m.model_dump(mode="json") for m in mapping]},
            out / "owasp-mapping.json",
        )
    )

    paths.append(
        _dump(
            {
                "assessment_summary": summary.model_dump(mode="json"),
                "pt_test_plan": [a.model_dump(mode="json") for a in assignments],
            },
            out / "pt-test-plan.json",
        )
    )

    paths.append(
        _dump(
            {"attack_vectors": [v.model_dump(mode="json") for v in vectors]},
            out / "attack-vectors.json",
        )
    )

    md = build_markdown(recon, summary, mapping, assignments, vectors)
    md_path = out / "report.md"
    md_path.write_text(md, encoding="utf-8")
    paths.append(md_path)

    html = build_html(recon, summary, mapping, assignments, vectors)
    html_path = out / "report.html"
    html_path.write_text(html, encoding="utf-8")
    paths.append(html_path)

    return paths


def build_full_plan(
    recon: NormalizedRecon,
    mapping: list[OwaspMappingItem],
    summary: PTAssessmentSummary,
    assignments: list[PTTestAssignment],
    vectors: list[AttackVector],
) -> PTPlan:
    """Bundle everything into a single :class:`PTPlan` object."""
    return PTPlan(
        assessment_summary=summary,
        owasp_mapping=mapping,
        pt_test_plan=assignments,
        attack_vectors=vectors,
    )
